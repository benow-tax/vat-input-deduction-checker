def run_check(df, cfg):
    for col in ['공급가액','세액','합계']:
        if col in df.columns:
            df[col]=pd.to_numeric(df[col].astype(str).str.replace(',','').str.strip(),errors='coerce').fillna(0)
    if '(세금)계산서일' in df.columns:
        df['_is_date']=pd.to_datetime(df['(세금)계산서일'],errors='coerce').notna()
    else:
        df['_is_date']=True
    actual=df[df['_is_date']].copy()
    results={'불공제_조정필요':[],'담당자_확인필요':[],'세무구분_오류':[],'적요_공란':[]}
    for _,row in actual.iterrows():
        tax=str(row.get('세무','') or ''); vat=float(row.get('세액',0) or 0)
        sup=float(row.get('공급가액',0) or 0); tot=float(row.get('합계',0) or 0)
        desc=str(row.get('적요','') or '').strip()
        done=tax in ['매입불공제','면세매입','영세매입','현금영수증매입','수입','카드(관리용/불공)']
        if not desc or desc=='nan':
            results['적요_공란'].append(row.to_dict())
        if tax in ['과세매입','카드매입'] and vat==0 and sup==tot:
            d=row.to_dict(); d['_오류유형']='[오류A] 세액=0인데 과세매입/카드매입'; d['_비고']='세액=0이면 영세매입·면세매입·매입불공제 중 하나로 변경 필요'
            results['세무구분_오류'].append(d); continue
        if tax in ['영세매입','면세매입'] and vat>0:
            d=row.to_dict(); d['_오류유형']='[오류B] 세액>0인데 영세매입/면세매입'; d['_비고']='세액이 있으면 과세매입 등으로 변경 필요'
            results['세무구분_오류'].append(d); continue
        if done or tax not in ['과세매입','카드매입']: continue
        learned_match = False
        for i, lkw in enumerate(cfg.get('learned_nontax',[])):
            if lkw and lkw.lower() in desc.lower():
                r_list = cfg.get('learned_nontax_reason',[])
                reason = r_list[i] if i < len(r_list) else ''
                d=row.to_dict(); d['_불공제사유']=reason; d['_비고']='[학습됨] 담당자 확인 후 불공제 등록된 항목'
                results['불공제_조정필요'].append(d)
                learned_match = True; break
        if learned_match: continue
        res=classify_row(row,cfg)
        if res is None: continue
        cat,reason,note=res
        d=row.to_dict(); d['_불공제사유']=reason; d['_비고']=note
        if cat=='불공제': results['불공제_조정필요'].append(d)
        elif cat=='담당자확인': results['담당자_확인필요'].append(d)
    learned_exempt_list = cfg.get('learned_exempt', [])
    learned_nontax_list = cfg.get('learned_nontax', [])
    learned_nontax_reason = cfg.get('learned_nontax_reason', [])

    # 신규 적요 패턴 감지 (기존 패턴에 없는 항목 → 신규패턴 섹션으로 이동)
    novel_items = []
    for key in ['불공제_조정필요','담당자_확인필요']:
        remaining = []
        for item in results[key]:
            desc = str(item.get('적요','') or '')
            novel, unknown_words = is_novel_desc(desc, learned_exempt_list)
            if novel:
                item['_원래분류'] = key
                item['_신규단어'] = ', '.join(unknown_words)
                item['_비고'] = f"[신규패턴] {', '.join(unknown_words)} → " + str(item.get('_비고',''))
                novel_items.append(item)
            else:
                remaining.append(item)
        results[key] = remaining
    results['신규패턴'] = novel_items

    # 각 섹션 내 비고 기준 1차 정렬 → 날짜 기준 2차 정렬
    for key in results:
        results[key].sort(key=lambda x: (
            str(x.get('_비고','') or ''),
            str(x.get('_오류유형','') or ''),
            str(x.get('(세금)계산서일','') or '')
        ))
    return results


