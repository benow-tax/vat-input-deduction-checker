            if kw:
                if kw not in learned_exempt:
                    learned_exempt.append(kw)
                    cfg2['learned_exempt'] = learned_exempt
                    st.session_state.config = cfg2
                    st.success(f"'{kw}' 공제 항목으로 등록됐습니다!")
                    st.rerun()
                else:
                    st.warning("이미 등록된 키워드입니다.")
