def _extract_words(desc):
    import re as _r
    s = _r.sub(r'\[.*?\]', '', str(desc))
    s = _r.sub(r'\d{4}[\\.-]\d{2}[\\.-]\d{2}', '', s)
    s = _r.sub(r'20\d{6}', '', s)
    s = _r.sub(r'\d{1,2}월', '', s)
    if '/' in s:
        parts = s.split('/'); s = parts[-1] if len(parts) > 1 else s
    return _r.findall(r'[가-힣]{2,}|[a-zA-Z]{3,}', s)

def is_novel_desc(desc, learned_exempt=None):
    if learned_exempt:
        for kw in learned_exempt:
            if kw and kw.lower() in str(desc).lower(): return False, []
    words = _extract_words(desc)
    if not words: return False, []
    unknown = [w for w in words if w not in _KNOWN_KEYWORDS]
    return (len(unknown)/len(words)) >= 0.5, unknown

